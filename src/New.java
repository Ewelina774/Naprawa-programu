public class New {

    boolean isEven (int liczba) {

        return liczba%2==0;
    }

    boolean isOdd (int liczba) {
        return liczba%2==1;
    }

    double circleField (double liczba) {
        return Math.PI*liczba*liczba;

    }

    int power (int liczba) {
        return liczba*liczba;
    }
}
